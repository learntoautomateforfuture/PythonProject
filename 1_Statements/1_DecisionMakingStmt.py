# if - else
a = 90
if (a < 10):
    print ("a is less than 10")
else:
    print ("a is greater than 10")

# if - elif - else
a = 50
if (a == 10):
    print(a)
elif ( a == 50):
    print(a)
else:
    print ("a is undefined")

# Nested if
a = 35
if (a != 0):
    if(a < 40):
        print("a is less than 40")
    else:
        print("a is greater than 40")
else:
    print("a is zero")
